// La base de la programación modular en la programación clásica.
// Programación modular implica no abordar el problema completo, sino
// descomponerlo en problemas más pequeños, más fáciles de solucionar uno a uno.

// Programación modular: Dividir el programa en un conjunto de módulos.
// Programar cada módulo de manera independiente.
// Cada módulo se encarga de una determinada tarea o función. De unos datos de
// entrada se produce unos resultados.
// La aplicación completa se convierte en una serie de módulos que se
// interconectan consiguiendo solventar el problema completo.

// En JS se dispone de funciones y objetos para implementar este paradigma.

// Una función es código de JS que realiza una determinada tarea a partir de
// unos datos de entrada (parámetros). 
// La tarea consiste en devolver un determinado resultado a partir de los datos
// de entrada, o realizar una acción determinada.
// Una función puede ser invocada una y otra vez. Se pueden usar desde
// distintas aplicaciones.
// Un código que utiliza funciones es más legible y facilita la detección de 
// errores. Permite ir mejorando y mejorando la función, para que todas las
// aplicaciones que la utilicen vean reflejada esa mejora.

// JS es un lenguaje asíncrono donde las instrucciones se ejecutan sin esperar
// a que la anterior termine. Esto dificulta aquellas tareas que requieren de
// un resultado anterior; es decir, tareas que requieran un modo síncrono de 
// trabajo. JS utiliza las funciones para solucionar este problema, dado que 
// las funciones son un código que se puede asociar a cualquier variable o 
// parámetro.